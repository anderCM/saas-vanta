module BulkImportsHelper
  def render_import_status_badge(status)
    config = case status
    when "pending"
      {
        classes: "bg-gray-100 text-gray-800 dark:bg-gray-900/30 dark:text-gray-400",
        label: "Pendiente"
      }
    when "processing"
      {
        classes: "bg-blue-100 text-blue-800 dark:bg-blue-900/30 dark:text-blue-400",
        label: "Procesando"
      }
    when "completed"
      {
        classes: "bg-green-100 text-green-800 dark:bg-green-900/30 dark:text-green-400",
        label: "Completado"
      }
    when "failed"
      {
        classes: "bg-red-100 text-red-800 dark:bg-red-900/30 dark:text-red-400",
        label: "Fallido"
      }
    else
      {
        classes: "bg-gray-100 text-gray-800 dark:bg-gray-900/30 dark:text-gray-400",
        label: status.humanize
      }
    end

    content_tag(:span, config[:label],
                class: "inline-flex items-center rounded-full px-2.5 py-0.5 text-xs font-medium #{config[:classes]}")
  end
end
